# Concatination function
str1 = "Bhavish"
str2 = "Reddy"
result = str1 + " " + str2
print(result)

# Length of a String

str3 = "My name is Divya Reddy"
length = len(str3)
print("Length of the name is:", length)

# Conversion to Upper and lower

uppercase = str3.upper()
lowercase = str3.lower()
print("Uppercase:", uppercase)
print("Lowercase:", lowercase)

# text replace

text = str3.replace("Divya", "Bhavish")
print("new text is:", text)